from adventofcode.year_2015.day_18_2015 import iterateBoard
from adventofcode.year_2015.day_18_2015 import iterateBoard2


def test_part_one():
    t1 = iterateBoard(
        [
            [".", "#", ".", "#", ".", "#"],
            [".", ".", ".", "#", "#", "."],
            ["#", ".", ".", ".", ".", "#"],
            [".", ".", "#", ".", ".", "."],
            ["#", ".", "#", ".", ".", "#"],
            ["#", "#", "#", "#", ".", "."],
        ],
        4,
    )
    count = sum(lst.count("#") for lst in t1)
    assert t1 == [
        [".", ".", ".", ".", ".", "."],
        [".", ".", ".", ".", ".", "."],
        [".", ".", "#", "#", ".", "."],
        [".", ".", "#", "#", ".", "."],
        [".", ".", ".", ".", ".", "."],
        [".", ".", ".", ".", ".", "."],
    ]
    assert count == 4


