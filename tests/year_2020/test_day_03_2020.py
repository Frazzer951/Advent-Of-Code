from adventofcode.year_2020.day_03_2020 import check_slope


def test_check_slope():
    map = [
        '..##.......',
        '#...#...#..',
        '.#....#..#.',
        '..#.#...#.#',
        '.#...##..#.',
        '..#.##.....',
        '.#.#.#....#',
        '.#........#',
        '#.##...#...',
        '#...##....#',
        '.#..#...#.#',
    ]
    assert 7 == check_slope(3, 1, map)
